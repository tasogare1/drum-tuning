    <span style="font-size: 40px; background-color: yellow">はじめに</span>
<br>
・とにかくまず使ってみたい方は、説明を読まずにさっそく使ってみてください。
<br>
<br>
・ドラムのチューニングに困っていませんか？耳に頼ってチューニングをし、失敗に終わっていませんか？　
<br>
<br>
・ドラムはギター等の他の楽器とは違って音階を決めてチューニングする楽器ではありません。<br>
その分感覚や耳に頼ることになるので、手さぐりになりやすく、いいチューニングができたとしてもまたチューニングをする際に同じようなチューニングを再現することは非常に難しいです。
<br>
<br>
・「ドラム　チューニング」と検索すると、真偽不明の個人の経験則による方法論が溢れています。<br>
　例を出すと、「ピッチに影響するのは打面」「裏面が響きや余韻に大きな影響を与えている」<br>
　「タムは表と裏の音程が同じになるようにチューニング」<br>「音程を変えたい場合は、表側を調整する」<br>
　「ロックでは裏より表を高くすることが多い」などといったものです。<br>確かに一理あるものもありますが、じゃあ具体的にはどのくらいの上下比で？表を高くする理由は？等と問いかけたくなります。
　具体性に欠けるものが極めて多いです。
<br>
<br>
・割愛しますが、実はドラムの上下ヘッドの周波数と基音(真ん中を叩いた音)には関係があります。<br>
　例えば、14インチのスネアの打面を280Hz、裏面を370Hzにすると、基音は約190Hzと決まります。<br>
　つまり、ドラムのチューニングは周波数によって科学的に再現することができるのです。
<br>
<br>
・上下ヘッドの貼り方に関して、2:3、3:4、5:8という比率を使っていますが、なぜかというとそれぞれ完全5度、完全4度、半6度の周波数比であるからです。<br>
　これらの周波数比にすることで、共鳴しやすくなります。例えばドとソは完全5度の関係にあり、共鳴しやすくなります。<br>
　また、周波数比をそろえることで、それぞれのタムの「音の形」がそろいやすくなります。例えば12“タムがトゥーンと伸びるのに13”タムがべたっとした音になる、といった事例を避けることができます。<br>
　そして、なぜ上<下の方が好ましいかというと、上ヘッドが高くなるにつれ高次の倍音が発生しやすく、耳障りになることが多いからです。<br>
　高次の倍音は揺れやすく、その倍音同士が混じってさらに耳障りになります。アコースティッキーなジャズをのぞいて、上ヘッドを高くすることは実際には滅多にありません。<br>
　ジャズの場合は、算出された周波数を上下逆にチューニングすればいいでしょう。
<br>
<br>
・周波数に関しては、専用のドラムチューナーを使うのが好ましいですが、<br>高いので、youtubeで「165Hz」と具体的なHzを検索するとその周波数を流し続けている動画が見つかるので、それを使うことをオススメします。
<br>
<br>
・周波数を使って数値的にチューニングをするのは、あくまで一つの方法でしかありません。<br>しかし、再現性を重視するには数値的にやるのが手っ取り早いです。参考程度に活用してください。<br>
　ドラムのチューニングに悩んでいる人のお役に立てれば幸いです。
