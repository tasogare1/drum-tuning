    <span style="font-size: 40px; background-color: yellow">スネアに関して</span><br>
・スネアのチューニングに関してですが、タムやバスドラのチューニングとは毛色が違います。<br>
タムやバスドラの場合、基音が音程感を演出しますが、スネアの場合は打面ヘッドの周波数が音程感を演出します。<br>
スネアサイドは一番低い音（基音）を決めます。ですので、タム類とは違ったアプローチが必要になります。 <br>
結論から言うと、まずは打面で音程感を決め、基音が極端に低く（高く）ならないように裏面を調整すればよいのです。<br>
逆に裏面をまず決め、打面を次に調整するのもいいと思います。<br>
「スネアサイドはG#にすると歯切れがよくなる」というのも耳にしたことがありますが、このように裏面をまず固定して打面を自分の好きなように決めるといった方法もいいと思います。<br>
私的にはこの方法を使うことが多いです。具体的に言うと、裏面を350~400Hzあたりにして、打面を好きなように決めます。370~380Hz付近にすることが多いです。
<br><br>
・まとめると、
①打面は音程感を決める。<br>
②裏面は一番低い音（基音）を決める。 基音があまり低く（高く）なりすぎないように裏面を設定する必要がある。<br>
　といったところです。 
<br><br>
・周波数の割り出し方に関しては、①まず打面を決め、②基音が低くも高くもなりすぎないような裏面の範囲を算出する という表示の仕方にしました。
<br><br>
・繰り返しになりますが、周波数を使って数値的にチューニングをするのは、あくまで一つの方法でしかありません。参考程度に活用してください。
